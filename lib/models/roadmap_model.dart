class RoadmapModel {

  final String roadmapId;

  final String title;

  final String category;

  final String description;


  final String targetCareer;


  final List<String> applicableStages;


  final List<String> steps;


  final List<String> relatedCourses;


  final List<String> relatedExams;


  final String duration;


  final String futureScope;


  final String officialSource;

  final String officialWebsite;


  final String academicYear;

  final String lastUpdated;

  final String verificationStatus;



  RoadmapModel({

    required this.roadmapId,

    required this.title,

    required this.category,

    required this.description,


    required this.targetCareer,


    required this.applicableStages,


    required this.steps,


    required this.relatedCourses,


    required this.relatedExams,


    required this.duration,


    required this.futureScope,


    required this.officialSource,

    required this.officialWebsite,


    required this.academicYear,

    required this.lastUpdated,

    required this.verificationStatus,

  });

}
